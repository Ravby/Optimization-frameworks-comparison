        function PopObj = CalObj(obj,PopDec)
            n = size(PopDec, 2);
            weights = 1:n;
            % Subtract the shift vector and compute the weighted sum of squares
            PopObj = sum(weights .* ((PopDec - obj.shift) .^ 2), 2);

            EventManager.getInstance().evaluationPerformed(PopObj, PopDec);
